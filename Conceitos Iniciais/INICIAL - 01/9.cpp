
/*
9) Helen recebe um salário de R$1.305,00 por mês.
    Seu patrão anunciou um aumento de 25% já para o próximo mês.
    Qual será o novo salário de Helen?
*/
#include <iostream>
using namespace std;

int main()
{
    float salario = 1305.0;
    float novo_salario = salario * 1.25;
    cout << "Novo salário de Helen: R$ " << novo_salario << endl;
    return 0;
}
