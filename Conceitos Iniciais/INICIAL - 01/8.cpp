
/*
8) Gustavo mora em Palmital e para chegar até a Fema ele precisa viajar 34km.
    O avô de Gustavo quer saber quantos metros existem entre Palmital e a Fema.
    Faça a transformação de km para metro.
*/
#include <iostream>
using namespace std;

int main()
{
    float km = 34.0;
    float metros = km * 1000.0;
    cout << "Distância entre Palmital e Fema: " << metros << " metros." << endl;
    return 0;
}
