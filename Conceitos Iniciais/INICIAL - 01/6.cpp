
/*
6) O Haras Mangalarga pretende trocar todas as ferraduras de seus 320 cavalos.
    Calcule a quantidade de ferraduras necessárias para equipar todos os cavalos do haras.
*/
#include <iostream>
using namespace std;

int main()
{
    int cavalos = 320;
    int ferraduras = cavalos * 4;
    cout << "Quantidade de ferraduras necessárias: " << ferraduras << endl;
    return 0;
}
