
/*
5) Eduardo e Mônica estão pensando em casamento e resolveram comprar um terreno.
    A Imobiliária Legião Urbana ofereceu terreno ao casal que possui 10 m de largura por 32 m de comprimento.
    Calcule a área do terreno.
*/
#include <iostream>
using namespace std;

int main()
{
    float largura = 10.0, comprimento = 32.0;
    float area = largura * comprimento;
    cout << "Área do terreno: " << area << " m²" << endl;
    return 0;
}
