#include <iostream>
using namespace std;

int main()
{
    float base, altura;
    cout << "Digite a base do paralelogramo: ";
    cin >> base;
    cout << "Digite a altura do paralelogramo: ";
    cin >> altura;
    cout << "Área do paralelogramo: " << base * altura << endl;
    return 0;
}
